
import fs from 'fs';
import path from 'path';
import ami from 'asterisk-manager';

const DATA_DIR = path.join(process.cwd(), 'src/data');
const EXTENSIONS_FILE = path.join(DATA_DIR, 'extensions.json');
const TARGET_CONF = '/etc/asterisk/pjsip_miac_users.conf';

// Настройки AMI (должны совпадать с manager.conf)
const amiConfig = {
  port: 5038,
  host: '127.0.0.1',
  user: 'miac',
  password: 'MiacAMI2026'
};

console.log('🚀 [BRIDGE] Мост МИАЦ.СВЯЗЬ запущен...');
console.log(`📂 [BRIDGE] Директория данных: ${DATA_DIR}`);

function generatePJSIPConfig(extensions) {
  let config = '; --- GENERATED BY MIAC.SVYAZ ---\n';
  config += '; DO NOT EDIT THIS FILE MANUALLY\n\n';

  extensions.forEach(ext => {
    const id = ext.id;
    const secret = ext.secret;

    // Секция AUTH
    config += `[${id}](!)\ntype=auth\nauth_type=userpass\npassword=${secret}\nusername=${id}\n\n`;

    // Секция AOR
    config += `[${id}](!)\ntype=aor\nmax_contacts=5\nremove_existing=yes\n\n`;

    // Секция ENDPOINT
    // Используем transport-udp-nat, который уже есть в основном pjsip.conf пользователя
    config += `[${id}](!)\ntype=endpoint\nauth=${id}\naors=${id}\ncontext=from-internal\ndisallow=all\nallow=ulaw,alaw,g722\ntransport=transport-udp-nat\n\n`;
    
    // Реальные объекты
    config += `[${id}](${id})\n`;
  });

  return config;
}

const asterisk = ami(amiConfig.port, amiConfig.host, amiConfig.user, amiConfig.password, true);

async function syncToAsterisk() {
  console.log('🔄 [BRIDGE] Начало синхронизации...');
  
  if (!fs.existsSync(EXTENSIONS_FILE)) {
    console.log('⚠️ [BRIDGE] Файл extensions.json не найден. Пропуск.');
    return;
  }

  try {
    const extensions = JSON.parse(fs.readFileSync(EXTENSIONS_FILE, 'utf8'));
    const configContent = generatePJSIPConfig(extensions);
    
    fs.writeFileSync(TARGET_CONF, configContent);
    console.log(`✅ [BRIDGE] Файл ${TARGET_CONF} обновлен. Абонентов: ${extensions.length}`);

    // Команды перезагрузки для Asterisk 17
    const commands = [
      'module reload res_pjsip.so',
      'pjsip reload',
      'core reload'
    ];

    for (const cmd of commands) {
      try {
        await new Promise((resolve, reject) => {
          asterisk.action({
            action: 'Command',
            command: cmd
          }, (err, res) => {
            if (err || res.response === 'Error') reject(err || res);
            else resolve(res);
          });
        });
        console.log(`🔄 [BRIDGE] Asterisk Command (${cmd}): OK`);
        break; // Если одна сработала, остальные не мучаем
      } catch (e) {
        // Пробуем следующую команду
      }
    }
  } catch (error) {
    console.error('❌ [BRIDGE] Ошибка при записи конфига:', error.message);
  }
}

// Следим за файлом данных
fs.watch(DATA_DIR, (eventType, filename) => {
  if (filename === 'extensions.json') {
    syncToAsterisk();
  }
});

// Первичная синхронизация при запуске
syncToAsterisk();

asterisk.on('error', (err) => {
  if (err.code === 'ECONNREFUSED') {
    console.log('❌ [BRIDGE] AMI Connection Error: Проверьте manager.conf и "manager reload"');
  } else {
    console.log('❌ [BRIDGE] AMI Error:', err);
  }
});
